        Data sharing also allows you to expand your influence in the field. When you opt to share
        your dataset, you receive a persistent DOI and, as others reuse those data, you get credit
        in the currency of our field – citations. In addition, when you opt to share your
        procedures, your expertise in obtaining consent, collecting data, coding, training staff
        to code or collect data…all of these become the foundation upon which other researchers,
        particularly students and early career scholars, can build.
      </p>
