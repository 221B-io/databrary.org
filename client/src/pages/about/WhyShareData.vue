      <div class="text-h5 text-primary text-weight-normal q-pt-md">
        Duty to Participants…and Other Scientists
      </div>
      <p>
        Adherence to the foundational Belmont principles that govern research ethics suggests
        that researchers must consider data sharing and reuse as a duty to participants
        (Brakewood &amp; Poldrack, 2013). Data sharing increases the beneficence of research
        studies by giving more "bang for the buck" – by sharing and reusing data, researchers
        maximize participants' contribution by continuing to make scientific use out of data
        that would otherwise sit dusty on the shelf.
      </p>
      <p>
        We also have a duty toward our fellow research scientists, and data sharing increases
        justice. Not all institutions and researchers have access to the necessary resources
        for primary data collection. Data sharing allows early career scholars and those with
        limited resources to bring their unique perspectives to new research questions that will
        accelerate the growth of scientific knowledge designed to improve the human condition.
      </p>
      <div class="text-h5 text-primary text-weight-normal q-pt-md">
        Better Science
      </div>
      <p>
        Transparency and reproducibility are foundational to the behavioral sciences, and video
        data sharing is the primary vehicle for ensuring we meet these goals. By allowing other
        researchers access to our data and our procedures, we foster replication, allowing us to
        be more certain of the broader applicability of our findings, or the ways in which
        contextual variation can lead to different findings. Data sharing increases standardization
        across researchers and labs, both in procedures (e.g., template language for data sharing
        releases; consensus on how to ask participants for permission to share) and content
        (e.g., definitions of developmental terms; break points of categorical variables), to
        enable comparisons of results across studies.
      </p>
      <p>
        Data sharing also greatly increases our ability to connect and collaborate with
        researchers across the world, because data can be uploaded prospectively from one
        lab and coded remotely by researchers in another. Data sharing is a powerful vehicle
        for establishing and promoting cross-national collaboration, and providing tools to
        researchers in other countries. Through the process of data sharing, researchers establish
        infrastructures for transparency in protocols and providing valuable data with colleagues
        and students in other countries.
      </p>
      <p>
        Finally, collaboration and reuse greatly expand our datasets, multiplying sample size
        (and power), and allowing us to examine cohort effects, and assess possible differences
        due to geographical location or population characteristics. For example, there are new,
        emerging international efforts to "crowd-source" psychological science, like
        <a href="https://osf.io/wx7ck/">the Many Labs project</a>,
        <a href="https://osf.io/rpw6d/">the Many Babies project</a>,
        <a href="http://talkbank.org/">TalkBank</a>,
        <a
